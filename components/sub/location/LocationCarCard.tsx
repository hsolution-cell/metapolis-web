function renderHighlightedLine(line: string, highlights: string[] = []) {
  const highlight = highlights.find((word) => line.includes(word));

  if (!highlight) {
    return line;
  }

  const segments = line.split(new RegExp(`(${highlight.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")})`));

  return segments.map((segment, index) => {
    if (!segment) return null;

    return (
      <span
        key={`${segment}-${index}`}
        className={segment === highlight ? "location_car_highlight" : undefined}
      >
        {segment}
      </span>
    );
  });
}

